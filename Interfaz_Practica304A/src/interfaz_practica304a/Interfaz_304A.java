    private void txtNCKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNCKeyTyped
        int key = evt.getKeyChar();
        
        boolean mayus = key >= 65 && key <= 90;
        boolean minus = key >= 97 && key <= 122;
        boolean nums = key >= 48 && key <= 57;
        
        if(!(mayus || minus || nums)){
            evt.consume();
        }
        if(txtNC.getText().trim().length() == 8){
            evt.consume();
        }
    }//GEN-LAST:event_txtNCKeyTyped

